#!/usr/bin/env python3
"""
Fetch Suricata rules from Rulezet and optionally install/reload them.

Examples:
  # Print matching Suricata rules
  python3 rulezet-suricata.py --search ransomware --print-rules

  # Save rules locally
  python3 rulezet-suricata.py --search ransomware --save-dir ./suricata-rules

  # Write all fetched rules into one file for Suricata to load
  python3 rulezet-suricata.py --search ransomware --output-file ./rulezet-suricata.rules

  # Install rules into Suricata rules directory and reload Suricata
  sudo python3 rulezet-suricata.py \
      --search ransomware \
      --suricata-rules-dir /etc/suricata/rules \
      --output-file /etc/suricata/rules/rulezet.rules \
      --reload-command 'suricatasc -c reload-rules'
"""

from __future__ import annotations

import argparse
import re
import shlex
import subprocess
import sys
from dataclasses import dataclass
from pathlib import Path
from typing import List, Optional

import requests

DEFAULT_API_BASE = "https://rulezet.org"
DEFAULT_RULE_DETAIL_URL_PREFIX = "https://rulezet.org/rule/detail_rule/"
DEFAULT_TIMEOUT = 30


@dataclass
class RuleEntry:
    uuid: str
    title: str
    description: str
    author: str
    creation_date: str
    format: str
    content: str

    @property
    def detail_url(self) -> str:
        uuid = self.uuid.strip()
        if not uuid:
            return ""
        return f"{DEFAULT_RULE_DETAIL_URL_PREFIX}{uuid}"


def eprint(*args: object, **kwargs: object) -> None:
    print(*args, file=sys.stderr, **kwargs)


def sanitize_filename(value: str) -> str:
    value = value.strip().replace(" ", "_")
    value = re.sub(r"[^A-Za-z0-9._-]+", "_", value)
    value = re.sub(r"_+", "_", value)
    return value[:180].strip("._-") or "rule"


def fetch_rules(
    search: str,
    api_base: str = DEFAULT_API_BASE,
    timeout: int = DEFAULT_TIMEOUT,
    verify_tls: bool = True,
) -> List[RuleEntry]:
    url = f"{api_base.rstrip('/')}/api/rule/public/search"
    headers = {"accept": "application/json"}
    params = {"search": search}

    try:
        response = requests.get(
            url,
            headers=headers,
            params=params,
            timeout=timeout,
            verify=verify_tls,
        )
        response.raise_for_status()
    except requests.RequestException as exc:
        raise RuntimeError(f"HTTP request failed: {exc}") from exc

    try:
        payload = response.json()
    except ValueError as exc:
        raise RuntimeError("API did not return valid JSON") from exc

    results = payload.get("results", [])
    if not isinstance(results, list):
        raise RuntimeError("Unexpected API response: 'results' is not a list")

    out: List[RuleEntry] = []
    for item in results:
        if not isinstance(item, dict):
            continue
        if str(item.get("format", "")).lower() != "suricata":
            continue
        content = item.get("content")
        if not isinstance(content, str) or not content.strip():
            continue

        out.append(
            RuleEntry(
                uuid=str(item.get("uuid", "")),
                title=str(item.get("title", "")),
                description=str(item.get("description", "")),
                author=str(item.get("author", "")),
                creation_date=str(item.get("creation_date", "")),
                format="suricata",
                content=content.strip() + "\n",
            )
        )

    return out


def print_rules(rules: List[RuleEntry]) -> None:
    for idx, rule in enumerate(rules, start=1):
        print(f"===== RULE {idx} =====")
        print(f"Title       : {rule.title}")
        print(f"UUID        : {rule.uuid}")
        if rule.detail_url:
            print(f"Rule URL    : {rule.detail_url}")
        print(f"Author      : {rule.author}")
        print(f"Created     : {rule.creation_date}")
        print(f"Description : {rule.description}")
        print(rule.content.rstrip())
        print()


def save_individual_rules(rules: List[RuleEntry], save_dir: Path) -> List[Path]:
    save_dir.mkdir(parents=True, exist_ok=True)
    written: List[Path] = []

    for rule in rules:
        base = sanitize_filename(rule.title or rule.uuid or "rule")
        suffix = sanitize_filename(rule.uuid) if rule.uuid else "no_uuid"
        path = save_dir / f"{base}__{suffix}.rules"
        path.write_text(rule.content, encoding="utf-8", newline="\n")
        written.append(path)

    return written


def write_aggregate_rules_file(rules: List[RuleEntry], output_file: Path) -> None:
    output_file.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        "# Auto-generated by rulezet-suricata.py",
        f"# Total rules: {len(rules)}",
        "",
    ]

    for rule in rules:
        lines.append(f"# title: {rule.title}")
        if rule.uuid:
            lines.append(f"# uuid: {rule.uuid}")
        if rule.detail_url:
            lines.append(f"# source: {rule.detail_url}")
        lines.append(rule.content.rstrip())
        lines.append("")

    output_file.write_text("\n".join(lines).rstrip() + "\n", encoding="utf-8")


def run_command(command: str) -> int:
    try:
        proc = subprocess.run(
            shlex.split(command),
            capture_output=True,
            text=True,
            check=False,
        )
    except Exception as exc:
        raise RuntimeError(f"Failed to run command {command!r}: {exc}") from exc

    if proc.stdout:
        print(proc.stdout.rstrip())
    if proc.stderr:
        eprint(proc.stderr.rstrip())

    return proc.returncode


def build_arg_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description=(
            "Fetch Suricata rules from Rulezet and optionally save/install/reload them."
        )
    )
    parser.add_argument(
        "--search",
        required=True,
        help="Search term sent to Rulezet, e.g. ransomware, CVE-2025-XXXX",
    )
    parser.add_argument(
        "--api-base",
        default=DEFAULT_API_BASE,
        help=f"Rulezet base URL (default: {DEFAULT_API_BASE})",
    )
    parser.add_argument(
        "--timeout",
        type=int,
        default=DEFAULT_TIMEOUT,
        help=f"HTTP timeout in seconds (default: {DEFAULT_TIMEOUT})",
    )
    parser.add_argument(
        "--print-rules",
        action="store_true",
        help="Print fetched Suricata rules to stdout",
    )
    parser.add_argument(
        "--save-dir",
        type=Path,
        help="Directory where fetched rules are saved as individual .rules files",
    )
    parser.add_argument(
        "--output-file",
        type=Path,
        help=(
            "Write all fetched rules into a single rules file (for Suricata loading), "
            "for example /etc/suricata/rules/rulezet.rules"
        ),
    )
    parser.add_argument(
        "--suricata-rules-dir",
        type=Path,
        help=(
            "Optional Suricata rules directory to ensure exists (for convenience), "
            "for example /etc/suricata/rules"
        ),
    )
    parser.add_argument(
        "--test-command",
        default=None,
        help=(
            "Optional command to validate configuration after writing rules, "
            "example: 'suricata -T -c /etc/suricata/suricata.yaml -S /etc/suricata/rules/rulezet.rules'"
        ),
    )
    parser.add_argument(
        "--reload-command",
        default=None,
        help="Optional command to reload Suricata rules, example: 'suricatasc -c reload-rules'",
    )
    parser.add_argument(
        "--insecure",
        action="store_true",
        help="Disable TLS certificate verification",
    )
    return parser


def main() -> int:
    parser = build_arg_parser()
    args = parser.parse_args()

    try:
        rules = fetch_rules(
            search=args.search,
            api_base=args.api_base,
            timeout=args.timeout,
            verify_tls=not args.insecure,
        )
    except Exception as exc:
        eprint(f"[!] Failed to fetch rules: {exc}")
        return 1

    if not rules:
        eprint("[!] No Suricata rules found for that search.")
        return 2

    eprint(f"[+] Found {len(rules)} Suricata rule(s) for search={args.search!r}")

    if args.print_rules:
        print_rules(rules)

    if args.suricata_rules_dir:
        args.suricata_rules_dir.mkdir(parents=True, exist_ok=True)
        eprint(f"[+] Ensured rules directory exists: {args.suricata_rules_dir}")

    if args.save_dir:
        try:
            written = save_individual_rules(rules, args.save_dir)
        except Exception as exc:
            eprint(f"[!] Failed to save individual rules: {exc}")
            return 1
        for path in written:
            eprint(f"[+] Saved: {path}")

    if args.output_file:
        try:
            write_aggregate_rules_file(rules, args.output_file)
        except Exception as exc:
            eprint(f"[!] Failed to write output file: {exc}")
            return 1
        eprint(f"[+] Wrote aggregate Suricata rules file: {args.output_file}")

    if args.test_command:
        eprint(f"[+] Running test command: {args.test_command}")
        code = run_command(args.test_command)
        if code != 0:
            eprint(f"[!] Test command failed with exit code {code}")
            return code

    if args.reload_command:
        eprint(f"[+] Running reload command: {args.reload_command}")
        code = run_command(args.reload_command)
        if code != 0:
            eprint(f"[!] Reload command failed with exit code {code}")
            return code

    if not (args.print_rules or args.save_dir or args.output_file):
        eprint(
            "[i] Nothing was written yet. Use --print-rules, --save-dir, "
            "and/or --output-file to consume fetched rules."
        )

    return 0


if __name__ == "__main__":
    sys.exit(main())
